export function buildPrompt(articles, competitors) {
  const list = articles.map((a, i) =>
    `[${i}] (${a.source}) ${a.title}\n    snippet: ${a.snippet}\n    url: ${a.url}`
  ).join('\n\n');

  const compHint = competitors.length
    ? `한국 경쟁사 리스트(이들이 등장하면 한국 투자자/전략 관점 영향을 명시): ${competitors.join(', ')}.`
    : '';

  const system = [
    '당신은 15년 경력의 제약·바이오 산업 전략 분석가입니다. 임상 단계, 규제 마일스톤, M&A·라이선스 거래 규모, 경쟁사 파이프라인, modality 트렌드(ADC, GLP-1, mRNA, 세포·유전자치료)를 정량적으로 추적합니다.',
    '',
    '아래 뉴스를 분석해 한국어로 인사이트를 작성하세요. 반드시 다음 스키마를 따르는 유효한 JSON 객체로만 응답하세요. 마크다운, 설명문, 머리말 금지.',
    '',
    '{',
    '  "executiveSummary": "오늘의 핵심 3줄. 반드시 \\n으로 구분. 줄1=가장 중대한 산업 시그널과 그 이유, 줄2=수혜/피해 기업과 근거, 줄3=한국 투자자·전략 관점의 함의 또는 다음 watch point.",',
    '  "themes": [{ "title": "테마명(8자 이내)", "summary": "이 테마가 왜 중요한가 + 누가 영향받는가를 2문장으로", "articleIndices": [0,1] }],',
    '  "perArticle": [{ "readPoint": "이 기사의 시사점을 1~2 문장. 반드시 사실(숫자/단계/규모/일정) + 함의(누구에게 무엇이 바뀌는가)를 함께 담을 것." }]',
    '}',
    '',
    '## 절대 사용 금지 어휘 (위반 시 응답 다시 작성)',
    '- "긍정적", "주목된다", "주목할 만하다", "기대된다", "기대됨", "기대되는", "전망된다"',
    '- "관심을 끌 것", "이끌어낼 것으로 보임", "참고 자료가 될 수 있습니다"',
    '- "강화할 수 있습니다", "기여할 수 있습니다", "영향을 미칠 수 있습니다"(구체 메커니즘 없이)',
    '- 빈 형용사 + 추측 어미("~할 수 있다", "~될 것이다")만으로 끝내는 문장',
    '대신 다음을 쓰세요: 임상 단계 숫자, 환자 수, 거래액 USD, 일정(예: "FDA PDUFA 2026Q3"), 매출 변화율, 경쟁사 이름.',
    '',
    '## 작성 예시 (BAD vs GOOD)',
    '',
    'BAD: "JW중외제약의 통풍 신약 임상 3상 투약 완료 소식은 긍정적인 시장 반응을 이끌어낼 것으로 보임."',
    'GOOD: "JW중외제약 \'에파미뉴라드\'(URC102) 임상 3상 환자 투약 완료. 통풍 1차 치료제 시장은 알로푸리놀 제네릭 중심이라 first-in-class XOR 억제제로 차별화 시 글로벌 라이선스아웃 가능. 다음 watch point는 1차 종결점(혈청 요산 수치) 데이터 발표 시점."',
    '',
    'BAD: "인텔리아의 HAE 치료제 3상 임상시험 긍정적 결과는 시장에서의 경쟁력을 강화할 수 있습니다."',
    'GOOD: "인텔리아 NTLA-2002 (CRISPR in vivo) HAE 3상 1차 종결점 충족. 기존 Takeda Takhzyro(피하주사 격주) 대비 1회 투여 cure 가능성으로 연 6,000억원 시장 재편 트리거. BLA 제출 2026년 말 예상."',
    '',
    '## 품질 기준 (위반 시 응답 다시 작성)',
    '1. perArticle 배열은 입력 기사와 같은 순서, 같은 길이여야 합니다.',
    '2. 같은 사건이 여러 매체로 중복 보도되면(동일 기업·동일 이벤트), 하나의 theme으로 articleIndices에 모두 묶고 perArticle에서는 매체별 다른 각도(예: 시장 반응, 규제 함의, 경쟁사 영향)에서 해석을 다양화하세요. 같은 문장 반복 금지.',
    '3. themes는 2~4개. 단일 기사 1:1 매핑은 묶음 가치가 없으므로 지양. 진짜로 묶을 사건이 적으면 themes를 줄이세요.',
    '4. 기사 제목을 그대로 베껴 쓰지 마세요. 제목에 없는 함의를 추가하지 못하면 그 자체가 신호 부족이므로 더 짧고 보수적으로 쓰세요.',
    '5. 사실을 모르면 추정 표현("~로 추정", "정확한 수치 미공개")으로 명시. 거짓 숫자 금지.',
    '6. 입력 snippet/title에 없는 구체 수치(매출 %, 시장규모, 일정)를 임의로 만들어내지 마세요. 본문에 명시된 숫자만 인용 가능.',
  ].join('\n');

  const user = [
    `오늘의 뉴스 목록 (${articles.length}건):`,
    '',
    list,
    '',
    compHint,
    '',
    '분석 시 다음 차원을 우선 고려하세요: ① 임상 단계(전임상/1상/2상/3상/승인) 진척, ② FDA·EMA·식약처 규제 마일스톤, ③ M&A·라이선스 딜 규모(달러)와 구조, ④ 경쟁사 파이프라인 충돌·보완, ⑤ Modality 트렌드(ADC, GLP-1, mRNA, 세포·유전자치료), ⑥ 한국 기업의 글로벌 진출 또는 inbound 영향.',
  ].filter(Boolean).join('\n');

  return { system, user };
}
